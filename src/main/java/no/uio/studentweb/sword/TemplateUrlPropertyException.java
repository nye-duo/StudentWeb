package no.uio.studentweb.sword;

public class TemplateUrlPropertyException extends DiscoveryException
{
	public TemplateUrlPropertyException()
	{
		super();
	}

	public TemplateUrlPropertyException(String message)
	{
		super(message);
	}

	public TemplateUrlPropertyException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public TemplateUrlPropertyException(Throwable cause)
	{
		super(cause);
	}
}
